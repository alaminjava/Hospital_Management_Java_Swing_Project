    public void autopatientid() {
        int lastid;
        try {
            dbcon = new dbconnection();
            Connection con = dbcon.dbconnection();
            String query = "SELECT MAX(patient_id) FROM patient";
            PreparedStatement st = con.prepareStatement(query);
            ResultSet rs = st.executeQuery();
            if (rs.next()) {
                lastid = rs.getInt(1);
                lastid++;
                patientid.setText(Integer.toString(lastid));
            }
        } catch (Exception e) {
        }
    }

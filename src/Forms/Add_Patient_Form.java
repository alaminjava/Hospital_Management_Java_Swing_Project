    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
        if (bloodgroup.getSelectedItem() == "Select Blood Group") {
            JOptionPane.showMessageDialog(null, "Please Select Blood Group");
        } else if (name.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please Enter Name");
        } else if (age.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please Enter Age");
        } else if (phone.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please Enter Phone");
        } else if (email.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please Enter Email");
        } else if (address.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please Enter Address");
        } else {
            try {
                dbcon = new dbconnection();
                Connection con = dbcon.dbconnection();
                String query = "INSERT INTO patient "
                        + "(patient_id,date,Name,Age,Gender,Blood_Group,"
                        + "Contact,Email,Address,Remarks) "
                        + "VALUES (?,?,?,?,?,?,?,?,?,?)";
                PreparedStatement st = con.prepareStatement(query);
                st.setInt(1, Integer.parseInt(patientid.getText()));
                st.setString(2, nowdate.getText());
                st.setString(3, name.getText());
                st.setString(4, age.getText());
                if (male.isSelected()) {
                    st.setString(5, "Male");
                } else if (female.isSelected()) {
                    st.setString(5, "Female");
                } else {
                    st.setString(5, "Other");
                }
                st.setString(6, bloodgroup.getSelectedItem().toString());
                st.setString(7, phone.getText());
                st.setString(8, email.getText());
                st.setString(9, address.getText());
                st.setString(10, remarks.getText());
                st.executeUpdate();
                JOptionPane.showMessageDialog(null, "Save Sucessfull");
                con.close();
            } catch (SQLException e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_saveActionPerformed

